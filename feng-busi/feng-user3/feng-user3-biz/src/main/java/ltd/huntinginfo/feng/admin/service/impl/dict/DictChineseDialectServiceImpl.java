package ltd.huntinginfo.feng.admin.service.impl.dict;


import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
import com.baomidou.mybatisplus.extension.service.impl.ServiceImpl;
import lombok.extern.slf4j.Slf4j;
import ltd.huntinginfo.feng.admin.api.entity.dict.DictChineseDialect;
import ltd.huntinginfo.feng.admin.mapper.dict.DictChineseDialectMapper;
import ltd.huntinginfo.feng.admin.service.dict.DictChineseDialectService;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.cache.annotation.CacheConfig;
import org.springframework.cache.annotation.CacheEvict;
import org.springframework.cache.annotation.Cacheable;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.stereotype.Service;
import org.springframework.util.CollectionUtils;

import java.util.List;
import java.util.concurrent.TimeUnit;

import jakarta.annotation.PostConstruct;

@Slf4j
@Service
@CacheConfig(cacheNames = "dict:chinese_dialect")
public class DictChineseDialectServiceImpl 
    extends ServiceImpl<DictChineseDialectMapper, DictChineseDialect> 
    implements DictChineseDialectService {

    @Autowired
    private RedisTemplate<String, Object> redisTemplate;

    private static final String ALL_CACHE_KEY = "all";
    private static final long CACHE_EXPIRE_HOURS = 24;

    @Override
    @Cacheable(key = "#code")
    public DictChineseDialect getByCode(String code) {
        try {
            QueryWrapper<DictChineseDialect> wrapper = new QueryWrapper<>();
            wrapper.eq("code", code);
            return getOne(wrapper);
        } catch (Exception e) {
            log.error("获取汉语方言失败，code: {}", code, e);
            return null;
        }
    }

    @Override
    @Cacheable(key = ALL_CACHE_KEY)
    public List<DictChineseDialect> getAllValidItems() {
        QueryWrapper<DictChineseDialect> wrapper = new QueryWrapper<>();
        wrapper.orderByAsc("code");
        return baseMapper.selectList(wrapper);
    }

    @Override
    @CacheEvict(allEntries = true)
    public void refreshCache() {
        log.info("刷新汉语方言字典缓存");
    }

    /**
     * 初始化缓存数据
     */
    @PostConstruct
    public void initCache() {
        String lockKey = "lock:dict:chinese_dialect:init";
        try {
            // 尝试获取分布式锁
            Boolean locked = redisTemplate.opsForValue().setIfAbsent(lockKey, "1", 5, TimeUnit.MINUTES);
            if (Boolean.TRUE.equals(locked)) {
                List<DictChineseDialect> allDialects = this.getAllValidItems();
                if (!CollectionUtils.isEmpty(allDialects)) {
                    // 缓存所有数据
                    redisTemplate.opsForValue().set(
                        "dict:chinese_dialect:all", 
                        allDialects,
                        CACHE_EXPIRE_HOURS, 
                        TimeUnit.HOURS
                    );
                    
                    // 缓存每个code的单独数据
                    allDialects.forEach(dialect -> {
                        redisTemplate.opsForValue().set(
                            "dict:chinese_dialect:" + dialect.getCode(),
                            dialect,
                            CACHE_EXPIRE_HOURS,
                            TimeUnit.HOURS
                        );
                    });
                }
            }
        } finally {
            // 释放锁
            redisTemplate.delete(lockKey);
        }
    }
}
